package org.example;

public class City {
    private String name;
    private float lat;
    private float lon;


    //Getters & Setters
    public String getName(){
        return name;
    }
    public void setName(String name_){
        this.name=name_;
    }
    public float getLat(){
        return lat;
    }
    public void setLat(float lat_){
        this.lat=lat_;
    }

    public float getLon(){
        return lon;
    }
    public void setLon(float lon_){
        this.lon=lon_;
    }


}
